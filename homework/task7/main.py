def main(input):
    q1 = (int(input[0], 0) & 0x3)
    q2 = (int(input[1], 0) & 0x3) << 2
    q3 = (int(input[2], 0) & 0x3) << 4
    q4 = (int(input[3], 0) & 0xF) << 6
    q5 = (int(input[4], 0) & 0x3FF) << 10
    return str(q5 | q4 | q3 | q2 | q1)


